@mcp.tool()
def generate_queue_status() -> str:
    """Get image generation queue status (running and pending jobs)."""
    try:
        client = _get_client()
        resp = client.get("generate/queue")
        running = resp.get("queue_running", [])
        pending = resp.get("queue_pending", [])
        lines = [
            f"執行中：{len(running)} 筆",
            *[f"  - {r.get('job_id', '?')}: {r.get('status', '?')}" for r in running],
            f"等候中：{len(pending)} 筆",
            *[f"  - {p.get('job_id', '?')}: {p.get('status', '?')}" for p in pending],
        ]
        return "\n".join(lines) if lines else "佇列為空"
    except Exception as e:
        return f"error: {e}"


@mcp.tool()
def get_generation_status(job_id: str) -> str:
    """Query generation job status, returns agent-friendly JSON. Completed jobs include artifacts[]; image jobs keep image_id/image_path. Failed custom workflows return structured node_errors or recording_error."""
    try:
        client = _get_client()
        resp = client.get(f"generate/job/{job_id}")
        status = resp.get("status", "unknown")
        if status == "completed":
            artifacts = resp.get("artifacts", [])
            has_non_image_artifact = any(
                artifact.get("artifact_type") != "image"
                for artifact in artifacts
                if isinstance(artifact, dict)
            )
            next_step = (
                "call get_gallery_artifact with an artifact id from artifacts[], then free_comfyui_memory"
                if has_non_image_artifact
                else "call get_gallery_image with image_id, then free_comfyui_memory"
            )
            return json.dumps(
                {
                    "ok": True,
                    "tool": "get_generation_status",
                    "job_id": job_id,
                    "status": "completed",
                    "image_id": resp.get("image_id"),
                    "image_path": resp.get("image_path", ""),
                    "artifacts": artifacts,
                    "next": next_step,
                },
                ensure_ascii=False,
            )
        elif status == "failed":
            node_errors = resp.get("node_errors", [])
            return json.dumps(
                {
                    "ok": False,
                    "tool": "get_generation_status",
                    "where": "comfyui",
                    "job_id": job_id,
                    "status": "failed",
                    "error": resp.get("error"),
                    "node_errors": node_errors,
                    "recording_error": resp.get("recording_error"),
                    "next": (
                        "fix the offending nodes (check names/inputs via get_node_schema) and resubmit generate_image_custom_workflow"
                        if node_errors
                        else "generation failed; inspect error and resubmit"
                    ),
                },
                ensure_ascii=False,
            )
        elif status in ("queued", "running"):
            return json.dumps(
                {
                    "ok": True,
                    "tool": "get_generation_status",
                    "job_id": job_id,
                    "status": status,
                    "prompt_id": resp.get("prompt_id"),
                    "next": "wait, then call get_generation_status again",
                },
                ensure_ascii=False,
            )
        else:
            return json.dumps(
                {
                    "ok": False,
                    "tool": "get_generation_status",
                    "where": "backend",
                    "job_id": job_id,
                    "error": f"Job not found or unexpected status: {status}",
                },
                ensure_ascii=False,
            )
    except Exception as e:
        return json.dumps(
            {
                "ok": False,
                "tool": "get_generation_status",
                "where": "backend",
                "job_id": job_id,
                "error": str(e),
            },
            ensure_ascii=False,
        )


@mcp.tool()
def cancel_job(job_id: str) -> str:
    """Cancel an image generation job that has not yet started (pending status). Running jobs cannot be cancelled."""
    try:
        client = _get_client()
        resp = client.delete(f"generate/queue/{job_id}")
        return resp.get("message", "已取消")
    except Exception as e:
        return f"error: {e}"


